/* Look up a Unicode glyph bitmap; returns NULL if not found. */
static const unsigned char *find_unicode_glyph(unsigned int cp)
{
    for (int i = 0; i < NUM_UNICODE_GLYPHS; i++) {
        if (s_unicode_glyphs[i].cp == cp)
            return s_unicode_glyphs[i].bm;
    }
    return NULL;
}

/* ── Extended text API ───────────────────────────────────────────────── */

/*
 * Draw one character from the 8×16 font, identified by Unicode codepoint.
 * ASCII codepoints (0x20-0x7E) are served from the built-in PC-OEM table.
 * Other codepoints are looked up in s_unicode_glyphs; unmapped codepoints
 * fall back to '?'.
 * smooth=1 applies alpha-blended fringe pixels on each lit edge pixel for
 * a softer appearance.
 */
static void draw_char8x16(const crossos_framebuffer_t *fb,
                           int x, int y,
                           unsigned int cp,
                           crossos_color_t color,
                           int scale,
                           int smooth)
{
    const unsigned char *rows;

    if (cp >= 0x20 && cp <= 0x7E) {
        rows = s_font8x16[(unsigned)(cp - 0x20)];
    } else {
        rows = find_unicode_glyph(cp);
        if (!rows)
            rows = s_font8x16[(unsigned)('?' - 0x20)]; /* fallback */
    }

    for (int row = 0; row < FONT8X16_H; row++) {
        unsigned char bits = rows[row];
        for (int col = 0; col < FONT8X16_W; col++) {
            if ((bits >> (7 - col)) & 1) {
                int px = x + col * scale;
                int py = y + row * scale;
                crossos_draw_fill_rect(fb, px, py, scale, scale, color);
            } else if (smooth && scale == 1) {
                /* For smooth mode, blend fringe: check if any adjacent
                 * bit in the same or adjacent rows is lit.             */
                int prev_col = col > 0 ? ((bits >> (8 - col)) & 1) : 0;
                int next_col = col < 7 ? ((bits >> (6 - col)) & 1) : 0;
                int prev_row = row > 0 ? ((rows[row-1] >> (7 - col)) & 1) : 0;
                int next_row = row < 15 ? ((rows[row+1] >> (7 - col)) & 1) : 0;
                if (prev_col || next_col || prev_row || next_row) {
                    crossos_color_t faded = color;
                    faded.a = 70;
                    put_pixel_blend(fb, x + col, y + row, faded);
                }
            }
        }
    }
}

void crossos_draw_text_ex(const crossos_framebuffer_t *fb,
                          int x, int y,
                          const char *text,
                          crossos_color_t color,
                          crossos_font_t font,
                          int scale)
{
    if (!fb || !text || scale <= 0) return;

    if (font == CROSSOS_FONT_GLYPH) {
        crossos_draw_text(fb, x, y, text, color, scale);
        return;
    }

    int smooth  = (font == CROSSOS_FONT_SMOOTH);
    int advance = FONT8X16_ADVANCE * scale;
    const char *p = text;
    int col = 0;
    unsigned int cp;

    while ((cp = utf8_next(&p)) != 0) {
        draw_char8x16(fb, x + col * advance, y, cp, color, scale, smooth);
        col++;
    }
}

int crossos_draw_text_width_ex(const char *text, crossos_font_t font, int scale)
{
    if (!text || scale <= 0) return 0;
    if (font == CROSSOS_FONT_GLYPH) return crossos_draw_text_width(text, scale);
    return utf8_char_count(text) * FONT8X16_ADVANCE * scale;
}

int crossos_draw_text_height_ex(crossos_font_t font, int scale)
{
    if (font == CROSSOS_FONT_GLYPH) return crossos_draw_text_height(scale);
    return FONT8X16_H * (scale > 0 ? scale : 1);
}

void crossos_draw_glyph(const crossos_framebuffer_t *fb,
                        int x, int y,
                        unsigned int codepoint,
                        crossos_color_t color,
                        crossos_font_t font,
                        int scale)
{
    if (!fb || scale <= 0) return;

    if (font == CROSSOS_FONT_GLYPH) {
        /* 5×7 font is ASCII-only; map non-ASCII to '?'. */
        char buf[2];
        buf[0] = (codepoint >= 0x20 && codepoint <= 0x7E)
                     ? (char)codepoint : '?';
        buf[1] = '\0';
        crossos_draw_text(fb, x, y, buf, color, scale);
        return;
    }

    int smooth = (font == CROSSOS_FONT_SMOOTH);
    draw_char8x16(fb, x, y, codepoint, color, scale, smooth);
}