/* ── UTF-8 decoder ───────────────────────────────────────────────────── */

/*
 * Decode one Unicode codepoint from the UTF-8 string pointed to by *p.
 * Advances *p past the consumed bytes.
 * Returns 0 when *p points at the NUL terminator (string end).
 * Returns U+FFFD (REPLACEMENT CHARACTER) for invalid or over-long sequences.
 */
static unsigned int utf8_next(const char **p)
{
    const unsigned char *s = (const unsigned char *)*p;
    unsigned int c = *s;

    if (c == 0)   { return 0; }        /* end of string                    */
    if (c < 0x80) { *p += 1; return c; } /* plain ASCII                    */

    if ((c & 0xE0) == 0xC0) {           /* 2-byte sequence U+0080..U+07FF  */
        unsigned int b1 = s[1];
        if ((b1 & 0xC0) != 0x80) { *p += 1; return 0xFFFD; }
        *p += 2;
        unsigned int cp = ((c & 0x1F) << 6) | (b1 & 0x3F);
        return cp >= 0x80 ? cp : 0xFFFD; /* reject over-long               */
    }

    if ((c & 0xF0) == 0xE0) {           /* 3-byte sequence U+0800..U+FFFF  */
        unsigned int b1 = s[1], b2 = s[2];
        if ((b1 & 0xC0) != 0x80 || (b2 & 0xC0) != 0x80) {
            *p += 1; return 0xFFFD;
        }
        *p += 3;
        unsigned int cp = ((c & 0x0F) << 12) | ((b1 & 0x3F) << 6) | (b2 & 0x3F);
        return cp >= 0x800 ? cp : 0xFFFD;
    }

    if ((c & 0xF8) == 0xF0) {           /* 4-byte sequence U+10000..U+10FFFF */
        unsigned int b1 = s[1], b2 = s[2], b3 = s[3];
        if ((b1 & 0xC0) != 0x80 || (b2 & 0xC0) != 0x80 || (b3 & 0xC0) != 0x80) {
            *p += 1; return 0xFFFD;
        }
        *p += 4;
        unsigned int cp = ((c & 0x07) << 18) | ((b1 & 0x3F) << 12)
                        | ((b2 & 0x3F) << 6)  | (b3 & 0x3F);
        return cp >= 0x10000 ? cp : 0xFFFD;
    }

    *p += 1;
    return 0xFFFD; /* continuation byte without a leading byte */
}

/* Count the number of Unicode codepoints in a UTF-8 string.
 * Uses utf8_next() so invalid sequences are treated the same way as
 * during rendering (each invalid run counts as one replacement glyph). */
static int utf8_char_count(const char *text)
{
    int n = 0;
    const char *p = text;
    while (utf8_next(&p) != 0) n++;
    return n;
}

